"""
============================================================================
 全局配置中心 — 微服务地址 · 过滤阈值 · 评分权重 · 输出设置
============================================================================

本文件是整个流水线唯一的"参数控制面板"。
修改阈值不需要改动任何业务逻辑代码，只改这里即可。

设计原则
--------
1. 所有魔法数字集中管理，不散落各个模块
2. 每个参数都有注释说明其生物学/工程意义
3. 按功能分为六个区块：微服务 · 硬过滤 · 评分 · 肽预筛 · 禁入区 · 输出

科学背景速览
------------
- 丝素蛋白骨架（silk fibroin scaffold）在 E. coli 中表达后形成 inclusion body，
  需要通过变性→复性流程重新折叠。在此过程中 β-sheet 重排可能把插入的功能肽"包埋"。
- 因此：功能肽应偏亲水（GRAVY < 0）、长度不宜过长（5–15 aa）、净电荷适中。
- 插入位点需避开 β-sheet 结晶区（poly-Ala）、二硫键风险区（Cys 密集）、疏水核心。
"""

