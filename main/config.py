# ═══════════════════════════════════════════════════════════════════════════════
# 四、功能肽预筛选阈值 — Step 2
# ═══════════════════════════════════════════════════════════════════════════════
#
# 在枚举 construct 之前，先对功能肽进行一轮理化性质粗筛。
# 这些阈值的科学依据来自 PROGRAM.md：
#
#   亲水性优先（GRAVY < 0）
#     - 亲水肽倾向于留在溶液-蛋白界面上，而非被 β-sheet 疏水核心包埋
#     - 疏水肽在复性过程中极易被埋入蛋白内部 → 功能丧失
#
#   长度限制（5–15 aa）
#     - 太短：缺乏足够的功能表位
#     - 太长：折叠自由度增加，可能干扰丝素骨架 β-sheet 形成
#
#   净电荷适中（-3 ~ +3）
#     - 极端电荷会影响融合蛋白整体 pI，干扰 IB 溶解和复性条件
#     - 过高正电荷可能非特异性吸附于带负电的细胞表面
#
#   pI 警告（6–8 不淘汰，仅标记警告）
#     - pI 在 6–8 的肽在复性缓冲液（通常 pH 7–8）中净电荷接近零
#     - 溶解性可能较差，需在复性时有意偏离 pH

# 长度限制（氨基酸残基数）
PEPTIDE_MIN_LENGTH = 5
PEPTIDE_MAX_LENGTH = 15

# 亲水性阈值：GRAVY < 0 为亲水，≥ 0 淘汰
# GRAVY = Grand Average of Hydropathicity，基于 Kyte-Doolittle 标度
PEPTIDE_MAX_GRAVY = 0.0

# 净电荷范围（pH 7 近似值）
PEPTIDE_MIN_CHARGE = -3
PEPTIDE_MAX_CHARGE = 3

# pI 警告区间（在此区间内的肽不淘汰，但输出 warning）
