# ═══════════════════════════════════════════════════════════════════════════════
# 五、禁入区（Forbidden Zone）检测规则 — Step 4
# ═══════════════════════════════════════════════════════════════════════════════
#
# 在 scaffold 上扫描三类禁止插入区域：
#
# 1. poly-Ala 区（β-sheet 结晶区）
#    连续丙氨酸形成 β-sheet 晶体，插入功能肽会打断晶体堆积 → 力学结构崩溃
#    阈值 ≥4 个连续 A 即标记（蜘蛛丝中 poly-Ala run 通常 4–10 个残基）
#
# 2. Cys 密集区（二硫键风险）
#    两个及以上 Cys 间距 ≤ 10 残基的区域
#    插入的肽若含 Cys，可能在此区域形成非预期二硫键 → 错误折叠
#
# 3. 疏水核心
#    滑动窗口（默认 5 残基）计算 Kyte-Doolittle 平均疏水性
#    连续 ≥3 个窗口均值 > 1.5 → 标记为疏水核心
#    功能肽插入疏水核心 → 复性时被包埋，功能丧失

# poly-Ala 最小连续长度（≥此值即禁止插入）
POLY_ALA_MIN_LEN = 4

# Cys 密集区判定：至少多少个 Cys
CYS_CLUSTER_COUNT = 2
# Cys 密集区判定：最大间距（残基数）
CYS_CLUSTER_WINDOW = 10

# 疏水核心扫描窗口大小（残基数）
FORBIDDEN_WINDOW = 5
# 窗口疏水性均值阈值（Kyte-Doolittle 标度）
HYDROPHOBIC_CORE_THRESHOLD = 1.5
# 连续疏水窗口数阈值（≥此值即标记为疏水核心）
HYDROPHOBIC_CONSECUTIVE_WINDOWS = 3


