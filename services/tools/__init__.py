# 【模块说明】
# ------------
# tools/ 文件夹存放的是各个"工具服务"的实现代码。
#
# 【什么是"工具服务"？】
# ---------------------
# 在 iGEM-silk 的场景下，"工具"是指：
#   - AnOxPePred：抗氧化能力预测
#   - ToxinPred3：毒性预测
#   - Hemopi2：溶血活性预测
#   - MHCflurry：抗原呈递预测
#   - 等等...
#
# 每个工具都做成一个独立的"微服务"：
#   - 运行在独立的进程里
#   - 监听独立的端口（如 8001、8002）
#   - 对外提供统一的 HTTP 接口
#   - 模型常驻内存（不用每次调用都重新加载模型）
#
# 【为什么需要独立的服务？】
# ------------------------
# 1. 隔离性：某个工具崩了不会影响其他工具
# 2. 并发性：可以同时调用多个工具（并行）
# 3. 扩展性：以后加新工具，不用动其他代码
# 4. 维护性：每个工具的代码、依赖都是独立的
#
# 【文件夹结构】
# -------------
# tools/
# └── anoxpepred/          ← 抗氧化工具的"微服务"
#     └── service.py       ← 这个工具的实现代码
#
# 未来添加更多工具，就在这个目录下继续创建子文件夹。
#
# 【如何添加新工具？】
# ------------------
# 1. 在 tools/ 下创建 your_tool_name/ 文件夹
# 2. 在 your_tool_name/ 下创建 service.py
# 3. 继承 BioToolService，实现 load_model() 和 predict_impl()
# 4. 在 orchestrator/registry.py 的 TOOL_REGISTRY 中添加配置
# 5. 重启服务，新工具就生效了