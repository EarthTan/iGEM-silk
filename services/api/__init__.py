# 【模块说明】
# ------------
# api/ 文件夹是对外提供 HTTP 服务的"前台"。
# 外部的网页、手机 App、其他程序，都是通过这个"前台"来使用系统的。
#
# 【main.py 是唯一的入口】
# -----------------------
# 这个文件夹只有一个文件 main.py，它是一个 FastAPI 应用。
# 它的作用是：
#   - 暴露 HTTP 接口（URL 路径）
#   - 接收请求（用户点餐）
#   - 调用 Orchestrator 处理（厨房做菜）
#   - 返回结果（出餐）
#
# 【提供的主要接口】
# -----------------
# GET  /health              → 检查服务是否活着
# GET  /tools               → 查看有哪些可用工具
# POST /predict             → 预测单个序列
# POST /predict/batch       → 批量预测多个序列
#
# 【类比】
# -------
# 想象一个餐厅：
#   - api/main.py = 前台服务员（接待客人、记录点单、端菜）
#   - orchestrator/ = 厨房（内部协调，指挥各个厨师）
#   - tools/ = 各个厨师（实际做菜的人）
#   - registry.py = 厨师名片（知道每个厨师的名字和位置）